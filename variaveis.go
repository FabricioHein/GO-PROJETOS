package main

import "fmt"

func main() {
    var numeroInteiro int
    var numeroDecimal float64
    var texto string
    var condicao bool

    numeroInteiro = 42
    numeroDecimal = 3.14
    texto = "Olá, Mundo!"
    condicao = true

    fmt.Println("Número Inteiro:", numeroInteiro)
    fmt.Println("Número Decimal:", numeroDecimal)
    fmt.Println("Texto:", texto)
    fmt.Println("Condição:", condicao)
}
